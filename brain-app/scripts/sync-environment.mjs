import { existsSync, readFileSync, writeFileSync } from 'node:fs';
import { resolve } from 'node:path';

const envPath = resolve(process.cwd(), '..', '.env');
const outputPath = resolve(process.cwd(), 'src', 'environments', 'environment.local.ts');

if (!existsSync(envPath)) {
  throw new Error(`Missing ${envPath}. Copy the repository .env example and configure it first.`);
}

const values = Object.fromEntries(
  readFileSync(envPath, 'utf8')
    .split(/\r?\n/)
    .map((line) => line.trim())
    .filter((line) => line && !line.startsWith('#') && line.includes('='))
    .map((line) => {
      const separator = line.indexOf('=');
      const key = line.slice(0, separator).trim();
      let value = line.slice(separator + 1).trim();
      if (
        (value.startsWith('"') && value.endsWith('"')) ||
        (value.startsWith("'") && value.endsWith("'"))
      ) {
        value = value.slice(1, -1);
      }
      return [key, value];
    }),
);

const supabaseUrl = values.NEXT_PUBLIC_SUPABASE_URL ?? '';
const supabaseAnonKey = values.NEXT_PUBLIC_SUPABASE_ANON_KEY ?? '';
if (!supabaseUrl || !supabaseAnonKey) {
  throw new Error('NEXT_PUBLIC_SUPABASE_URL and NEXT_PUBLIC_SUPABASE_ANON_KEY must be set in .env.');
}

writeFileSync(
  outputPath,
  `// Generated by scripts/sync-environment.mjs. Do not commit.\n` +
    `export const environment = {\n` +
    `  production: false,\n` +
    `  supabaseUrl: ${JSON.stringify(supabaseUrl)},\n` +
    `  supabaseAnonKey: ${JSON.stringify(supabaseAnonKey)},\n` +
    `} as const;\n`,
  'utf8',
);

console.log('Generated local Angular environment with public Supabase configuration.');
